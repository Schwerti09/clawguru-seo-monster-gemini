function main() {
  let touched = 0
  let updatedKeys = 0

  for (const [locale, map] of Object.entries(translations)) {
    const filePath = path.join(DICT_DIR, `${locale}.json`)
    if (!fs.existsSync(filePath)) continue

    const dict = readJson(filePath)
    let localChanges = 0

    for (const [keyPath, value] of Object.entries(map)) {
      setPath(dict, keyPath, value)
      localChanges += 1
    }

    if (localChanges > 0) {
      writeJson(filePath, dict)
      touched += 1
      updatedKeys += localChanges
      console.log(`[priority-translations] ${locale}.json: ${localChanges} keys updated`)
    }
  }

  console.log(`[priority-translations] done: ${touched} files updated, ${updatedKeys} keys updated`)
}

