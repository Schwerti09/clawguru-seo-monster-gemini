export type TranslatedRunbook = {
  title: string
  summary: string
  locale: Locale
}

/**
 * WORLD BEAST: Auto-translate runbook metadata via Gemini API.
 * Falls back to original text if Gemini is unavailable.
 */
export async function translateRunbook(opts: {
  slug: string
  title: string
  summary: string
  targetLocale: Locale
}): Promise<TranslatedRunbook> {
  const { slug, title, summary, targetLocale } = opts

  if (targetLocale === "de") {
    const result = { title, summary, locale: "de" as const }
    console.log("translateRunbook", { slug, result: "bypass:de" })
    return result
  }

  // NEXT-LEVEL UPGRADE 2026: All 14 locale names for Gemini translation
  const localeNames: Record<Locale, string> = {
    de: "German",
    en: "English",
    es: "Spanish",
    fr: "French",
    pt: "Portuguese",
    it: "Italian",
    ru: "Russian",
    zh: "Simplified Chinese",
    ja: "Japanese",
    ar: "Arabic",
    nl: "Dutch",
    hi: "Hindi",
    tr: "Turkish",
    pl: "Polish",
    ko: "Korean",
  }

  const geminiKey = process.env.GEMINI_API_KEY
  const geminiModel = process.env.GEMINI_MODEL || "gemini-1.5-flash"
  const geminiBase = (
    process.env.GEMINI_BASE_URL || "https://generativelanguage.googleapis.com/v1beta"
  ).replace(/\/$/, "")

  if (!geminiKey) {
    const result = { title, summary, locale: targetLocale }
    console.log("translateRunbook", { slug, result: "fallback:no_key" })
    return result
  }

  const prompt = [
    `Translate the following runbook title and summary from German to ${localeNames[targetLocale]}.`,
    "Keep technical terms (e.g. SSH, TLS, CORS, CVE, runbook) as-is.",
    "Return ONLY a JSON object: { \"title\": \"...\", \"summary\": \"...\" }",
    "",
    `Slug: ${slug}`,
    `Title: ${title}`,
    `Summary: ${summary}`,
  ].join("\n")

  try {
    const url = `${geminiBase}/models/${encodeURIComponent(geminiModel)}:generateContent?key=${encodeURIComponent(geminiKey)}`
    const res = await fetch(url, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        contents: [{ role: "user", parts: [{ text: prompt }] }],
        generationConfig: { temperature: 0.2, maxOutputTokens: 300 },
      }),
      signal: AbortSignal.timeout(15_000),
    })
    if (!res.ok) {
      console.warn("translateRunbook", { slug, status: res.status, result: "fallback:http" })
      return { title, summary, locale: targetLocale }
    }
    const data = await res.json()
    const text: string =
      data?.candidates?.[0]?.content?.parts
        ?.map((p: { text?: string }) => p?.text ?? "")
        .join("") ?? ""
    const jsonStr = text.replace(/```json|```/g, "").trim()
    const parsed = JSON.parse(jsonStr) as { title?: string; summary?: string }
    if (parsed.title && parsed.summary) {
      const result = { title: parsed.title, summary: parsed.summary, locale: targetLocale as Locale }
      console.log("translateRunbook", { slug, result: "ok" })
      return result
    }
  } catch (e: unknown) {
    console.error("translateRunbook error", { slug, error: e instanceof Error ? e.message : String(e) })
  }

  const result = { title, summary, locale: targetLocale }
  console.log("translateRunbook", { slug, result: "fallback:parse" })
  return result
}
